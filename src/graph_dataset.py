"""
src/graph_dataset.py

PyG Dataset for per-commit vulnerability detection graphs.

Each item is a pre-built HeteroData graph loaded from data_new/graph_ready/graphs/.
Split assignment comes from split_index.csv (generated by scripts/create_split_index.py).

Usage:
    from src.graph_dataset import VulnCommitDataset, make_loader

    train_ds = VulnCommitDataset(split_type='repo_split', split='train')
    val_ds   = VulnCommitDataset(split_type='repo_split', split='val')
    test_ds  = VulnCommitDataset(split_type='repo_split', split='test')

    train_loader = make_loader(train_ds, batch_size=128, is_train=True)
    val_loader   = make_loader(val_ds,   batch_size=256, is_train=False)

Split types:
    'repo_split'     — train: all except ImageMagick/OpenSC, val: ImageMagick, test: OpenSC
    'temporal_split' — train: earliest 70%, val: next 15%, test: latest 15% by author_date
"""

import warnings
from pathlib import Path

import pandas as pd
import torch
from torch.utils.data import WeightedRandomSampler
from torch_geometric.data import Dataset
from torch_geometric.loader import DataLoader

ROOT        = Path(__file__).parents[1]
GRAPHS_DIR  = ROOT / "data_new" / "graph_ready" / "graphs"
SPLIT_INDEX = ROOT / "data_new" / "graph_ready" / "split_index.csv"


class VulnCommitDataset(Dataset):
    """
    Lazy-loading heterogeneous graph dataset for commit-level VCC detection.

    Each call to get(idx) loads one .pt file from disk — suitable for HPC
    environments where loading all graphs into RAM upfront is wasteful.

    Args:
        split_type : 'repo_split' or 'temporal_split'
        split      : 'train', 'val', or 'test'
        graphs_dir : override path to .pt files (default: data_new/graph_ready/graphs/)
        split_index_path : override path to split_index.csv
    """

    def __init__(
        self,
        split_type: str = "repo_split",
        split: str = "train",
        graphs_dir: Path | str | None = None,
        split_index_path: Path | str | None = None,
        ablate_code_emb: bool = False,
        ablate_msg_emb: bool = False,
        ablate_fn_categorical: bool = False,
        ablate_fn_code_metrics: bool = False,
        ablate_file_code_metrics: bool = False,
    ):
        super().__init__()
        assert split_type in ("repo_split", "temporal_split"), \
            f"split_type must be 'repo_split' or 'temporal_split', got {split_type!r}"
        assert split in ("train", "val", "test"), \
            f"split must be 'train', 'val', or 'test', got {split!r}"

        self.graphs_dir = Path(graphs_dir or GRAPHS_DIR)
        self.split_type = split_type
        self.split_name = split
        self.ablate_code_emb = ablate_code_emb
        self.ablate_msg_emb = ablate_msg_emb
        self.ablate_fn_categorical = ablate_fn_categorical
        self.ablate_fn_code_metrics = ablate_fn_code_metrics
        self.ablate_file_code_metrics = ablate_file_code_metrics

        df = pd.read_csv(split_index_path or SPLIT_INDEX)
        mask = df[split_type] == split
        self.records = df[mask][["hash", "label"]].reset_index(drop=True)

        n_pos = int(self.records["label"].sum())
        n_neg = len(self.records) - n_pos
        print(
            f"[VulnCommitDataset] {split_type}/{split}: "
            f"{len(self.records):,} graphs  "
            f"(VCC={n_pos:,}, neg={n_neg:,}, ratio=1:{n_neg/max(n_pos,1):.1f})"
        )

    # ── PyG Dataset interface ──────────────────────────────────────────────

    def len(self) -> int:
        return len(self.records)

    def get(self, idx: int):
        row = self.records.iloc[idx]
        path = self.graphs_dir / f"{row['hash']}.pt"
        # weights_only=False required for HeteroData objects
        with warnings.catch_warnings():
            warnings.simplefilter("ignore", FutureWarning)
            data = torch.load(path, weights_only=False)
        # Defense-in-depth: replace any NaN/inf that slipped through graph building
        for store in data.node_stores:
            if store.x is not None and store.x.numel() > 0:
                store.x = torch.nan_to_num(store.x, nan=0.0, posinf=0.0, neginf=0.0)
        # Ablation: zero out GraphCodeBERT dims (dims 10–777) in function nodes
        if self.ablate_code_emb:
            fn_x = data["function"].x
            if fn_x is not None and fn_x.numel() > 0 and fn_x.size(1) > 10:
                fn_x[:, 10:] = 0.0
        # Zero out commit message embedding (dims 6–773), keep 6 numeric dims
        if self.ablate_msg_emb:
            commit_x = data["commit"].x
            if commit_x is not None and commit_x.numel() > 0 and commit_x.size(1) > 6:
                commit_x[:, 6:] = 0.0
        # Zero out fct_* one-hot dims 5–9 in function nodes
        if self.ablate_fn_categorical:
            fn_x = data["function"].x
            if fn_x is not None and fn_x.numel() > 0:
                fn_x[:, 5:10] = 0.0
        # Zero out function code metric dims 0–4: LOC, complexity, token_count, length, nesting
        if self.ablate_fn_code_metrics:
            fn_x = data["function"].x
            if fn_x is not None and fn_x.numel() > 0:
                fn_x[:, 0:5] = 0.0
        # Zero out file code metric dims 0–2: num_lines_of_code, complexity, token_count
        if self.ablate_file_code_metrics:
            file_x = data["file"].x
            if file_x is not None and file_x.numel() > 0:
                file_x[:, 0:3] = 0.0
        data.y = torch.tensor([int(row["label"])], dtype=torch.long)
        return data

    # ── helpers ───────────────────────────────────────────────────────────

    def label_counts(self) -> tuple[int, int]:
        """Returns (n_positive, n_negative)."""
        n_pos = int(self.records["label"].sum())
        return n_pos, len(self.records) - n_pos

    def pos_weight(self) -> float:
        """BCE pos_weight = neg/pos (for non-focal baselines)."""
        n_pos, n_neg = self.label_counts()
        return n_neg / max(n_pos, 1)


def make_loader(
    dataset: VulnCommitDataset,
    batch_size: int = 128,
    num_workers: int = 4,
    is_train: bool = True,
    pin_memory: bool = True,
) -> DataLoader:
    """
    Build a DataLoader for the given dataset split.

    For training: uses WeightedRandomSampler to balance VCC:neg in each batch.
    For val/test: sequential order, true class distribution (for unbiased metrics).

    Args:
        dataset    : VulnCommitDataset instance
        batch_size : graphs per batch (128 is safe on A100/V100)
        num_workers: parallel file-loading workers
        is_train   : if True, use WeightedRandomSampler; if False, sequential
        pin_memory : pin CPU tensors for faster GPU transfer (disable on CPU-only)
    """
    if is_train:
        n_pos, n_neg = dataset.label_counts()
        n = n_pos + n_neg
        # Inverse-frequency weights: each class contributes equally in expectation
        weights = [
            n / (2.0 * n_pos) if int(lbl) == 1 else n / (2.0 * n_neg)
            for lbl in dataset.records["label"]
        ]
        sampler = WeightedRandomSampler(
            weights=weights,
            num_samples=n,
            replacement=True,
        )
        return DataLoader(
            dataset,
            batch_size=batch_size,
            sampler=sampler,
            num_workers=num_workers,
            pin_memory=pin_memory,
            persistent_workers=(num_workers > 0),
        )
    else:
        return DataLoader(
            dataset,
            batch_size=batch_size,
            shuffle=False,
            num_workers=num_workers,
            pin_memory=pin_memory,
            persistent_workers=(num_workers > 0),
        )
