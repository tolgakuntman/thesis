"""
src/graph_dataset.py

PyG Dataset for per-commit vulnerability detection graphs.

Each item is a pre-built HeteroData graph loaded from data_new/graph_ready/graphs/.
Split assignment comes from split_index.csv (generated by scripts/create_split_index.py).

Usage:
    from src.graph_dataset import VulnCommitDataset, make_loader

    train_ds = VulnCommitDataset(split_type='repo_split', split='train')
    val_ds   = VulnCommitDataset(split_type='repo_split', split='val')
    test_ds  = VulnCommitDataset(split_type='repo_split', split='test')

    train_loader = make_loader(train_ds, batch_size=128, is_train=True)
    val_loader   = make_loader(val_ds,   batch_size=256, is_train=False)

Split types:
    'repo_split'     — train: all except ImageMagick/OpenSC, val: ImageMagick, test: OpenSC
    'temporal_split' — train: earliest 70%, val: next 15%, test: latest 15% by author_date
"""

